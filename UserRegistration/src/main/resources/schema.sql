Create table if not exists adminLogin(User_id int(11) primary key,
UserName VARCHAR(50),
Password Varchar(50));
	

Create table if not exists register(user_id int(11) primary key auto_increment ,
UserName VARCHAR(50),
PhoneNo Varchar(50),
DOB Varchar(50),
Gender Varchar(50),
City Varchar(50),
Pincode Varchar(50));

